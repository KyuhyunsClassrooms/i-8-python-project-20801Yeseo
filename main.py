# =====================================================================
# [파트 1] 데이터 선언 부 (프로그램의 가장 위쪽에 위치)
# =====================================================================
# 2차원 리스트: 행(소재), 열(이름, 토양, 바다, 퇴비화, 팁, 탄소절감률)
plastic_data = [
    ["PLA (옥수수 전분 계열)", 365, 730, 90, "일반 자연에선 잘 안 썩으니 반드시 전문 퇴비화 시설로 보내야 해요!", 70],
    ["PHA (미생물 합성 계열)", 180, 60, 30, "바다에서도 빠르게 분해되는 해양 생분해 인증 소재입니다.", 90],
    ["PBS (석유 기반 생분해)", 270, 500, 120, "유연성이 좋아 비닐봉투에 쓰이며, 토양 분해 조건이 좋습니다.", 55]
]


# =====================================================================
# [파트 2] 함수 정의 부 (기능별로 분리된 함수들을 모아두는 곳)
# =====================================================================

# 1. 메뉴판 출력 함수
def show_menu():
    print("\n===========================================")
    print("🌿 바이오 플라스틱 생분해 기간 예측 프로그램 🌿")
    print("===========================================")
    print("1. 등록된 바이오 플라스틱 종류 보기")
    print("2. 조건별 생분해 기간 예측하기")
    print("3. 프로그램 종료")
    print("===========================================")

# 2. 2차원 리스트 탐색 함수 (반복문과 조건문 활용)
def find_plastic_row(data, name):
    for row in data:
        if name in row[0]: 
            return row  # 일치하는 플라스틱 데이터 행을 찾으면 반환
    return None         # 끝까지 못 찾으면 None 반환

# 3. 온도 조건 가중치 계산 함수 (실제 계산 알고리즘)
def calculate_final_days(base_days, temp_choice):
    # 온도가 저온(1번)이면 1.5배, 고온(3번)이면 0.8배, 그 외에는 1배
    if temp_choice == "1":
        return int(base_days * 1.5)
    elif temp_choice == "3":
        return int(base_days * 0.8)
    else:
        return base_days

# 4. 종합 리포트 출력 함수
def print_report(name, final_days, tip, carbon_rate):
    print("\n===========================================")
    print(f"📋 {name} 생분해 예측 리포트")
    print("===========================================")
    print(f"- 최종 예상 소요 기간: 약 {final_days}일")
    
    if final_days >= 365:
        years = final_days // 365
        remaining_days = final_days % 365
        print(f"  (💡 대략 {years}년 {remaining_days}일 동안 썩어야 해요!)")
        
    print(f"- 환경 성적: 석유 플라스틱 대비 탄소 약 {carbon_rate}% 절감")
    print(f"- 🌍 올바른 배출 지침: {tip}")
    print("-------------------------------------------") 
    print("- ⚠️ 본 결과는 조건부 추정치이며, 실제 분해 기간은 환경 조건에 따라 크게 달라질 수 있습니다.") 
    print("===========================================")

# =====================================================================
# [파트 3] 메인 프로그램 제어 부 (프로그램의 가장 아래쪽에서 엔진 역할)
# =====================================================================
while True:
    show_menu()  # 1. 메뉴판을 띄웁니다.
    choice = input("원하는 메뉴 번호를 입력하세요: ")
    
    if choice == "1":
        print("\n[등록된 바이오 플라스틱 목록]")
        # 반복문으로 2차원 리스트를 돌며 이름과 탄소 절감률 출력
        for row in plastic_data:
            print(f"- {row[0]} (기존 석유 플라스틱 대비 탄소 {row[5]}% 절감)")
            
    elif choice == "2":
        print("\n[생분해 기간 예측 시뮬레이션]")
        search_name = input("조회할 바이오 플라스틱 이름을 입력하세요 (예: PLA, PHA): ")
        
        # [함수 호출] 입력한 이름으로 데이터를 찾습니다.
        target_plastic = find_plastic_row(plastic_data, search_name)
        
        # 예외 처리: 데이터가 없는 경우 판정
        if target_plastic == None:
            print("❌ 등록되지 않은 플라스틱 이름입니다. 다시 확인해 주세요.")
        else:
            print(f"🔍 '{target_plastic[0]}' 데이터를 확인했습니다.")
            
            # 환경 선택 받기
            print("\n[플라스틱이 버려질 환경을 고르세요]")
            print("1. 토양 / 2. 바다 / 3. 퇴비화 시설")
            env_choice = input("번호 입력: ")
            
            # 선택한 환경 번호에 따라 2차원 리스트의 적절한 열 번호에서 기본 일수를 가져옵니다.
            if env_choice == "1":
                base_days = target_plastic[1]  # 토양 열
            elif env_choice == "2":
                base_days = target_plastic[2]  # 바다 열
            elif env_choice == "3":
                base_days = target_plastic[3]  # 퇴비화 열
            else:
                print("⚠️ 잘못된 번호입니다. 기본값인 '토양' 기준으로 연산합니다.")
                base_days = target_plastic[1]
            
            # 온도 조건 선택 받기
            print("\n[환경의 평균 온도를 고르세요]")
            print("1. 저온(겨울) / 2. 적정 온도 / 3. 고온(여름)")
            temp_choice = input("번호 입력: ")
            
            # [함수 호출] 온도 가중치를 반영한 최종 일수 계산
            final_days = calculate_final_days(base_days, temp_choice)
            
            # [함수 호출] 최종 리포트 출력
            print_report(target_plastic[0], final_days, target_plastic[4], target_plastic[5])
            
    elif choice == "3":
        print("\n프로그램을 종료합니다. 친환경 생활을 실천해 주세요! 🌍")
        break  # 무한 루프 탈출
        
    else:
        print("\n⚠️ 잘못된 입력입니다. 1, 2, 3번 중에서만 입력해 주세요.")